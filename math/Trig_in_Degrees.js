function sind(theta){
	return Math.sin(theta/_rad)
}

function cosd(theta){
	return Math.cos(theta/_rad)
}

function tand(theta){
	return Math.tan(theta/_rad)
}

function asind(ratio){
	return Math.asin(ratio)*_rad
}

function acosd(ratio){
	return Math.acos(ratio)*_rad
}

function atand(ratio){
	return Math.atan(ratio)*_rad
}

function atan2d(num1, num2){
	return Math.atan2(num1, num2)*_rad
}


